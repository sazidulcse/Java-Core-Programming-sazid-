package Java5_Operator;

public class Relational_Operator_4 {
    public static void main(String[] args) {
      /*  Relational operateor structure
    <  OP1 < OP2
    > OP1 > OP2
    <= OP1 <= OP2
    >= OP1 >= OP2 
    == OP1 == OP2 
    != OP1 != OP2
*/
        //example// variable init
        int op1 , op2;
        op1 = 53; 
        op2 = 31;
        boolean result  = op1 >op2;
        System.out.println(result);

        result = op1<op2;
        System.out.println(result); 





    }
    
}
