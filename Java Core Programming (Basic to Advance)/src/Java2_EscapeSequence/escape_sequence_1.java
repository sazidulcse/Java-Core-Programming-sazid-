package Java2_EscapeSequence;

public class escape_sequence_1 {
    public static void main(String[] args) {

        // Use backspace (\b)
        System.out.println("hi  \b Backspace ");
        
        // Use tab (\t)
        System.out.println("Hay \t Tab");

        // Use newLine (\n)
        System.out.println("Hay \n NewLine ");
        
    }
    
}
