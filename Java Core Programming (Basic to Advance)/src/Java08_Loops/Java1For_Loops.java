package Java08_Loops;

public class Java1For_Loops {
    public static void main(String[] args) {
        // for Loops Structure
        // for (init:  condition<=10;  increment++)
        

        // increment for loops
        for( int i=1; i<=10; i++){
            System.out.println(i + "\t Hay For Loops");
        }

        // decrement for loops
        for(int j =10; j>=1; j-- ){
            System.out.println(j);
        }


        
    }
    
}
