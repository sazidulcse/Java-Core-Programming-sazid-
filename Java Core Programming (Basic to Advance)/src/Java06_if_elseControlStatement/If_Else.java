package Java06_if_elseControlStatement;

public class If_Else {
    public static void main(String[] args) {
        //init variable
        int num1, num2;
        num1 = 52; 
        num2 = 32; 
        if(num1<num2){
            System.out.println("True");
        }
        else{
            System.out.println("False");
        }

        if(num1%2==0){
            System.out.println("Even number");
        }
        else{
            System.out.println("Odd number");
        }
        



    }
    
}
